public class prettyPrinting {
    public static void main(String[] args) {

        float a = 212.123434f;
        System.out.printf("Formatted Float is %.2f",a); //round of as well

        System.out.printf("\nPie: %.3f", Math.PI);

        System.out.printf("\nHello my name is %s and maybe i am %s dumb ", "Something", "So" );



        /*
        %d: For integers (decimal integers).
        %f: For floating-point numbers (decimal numbers).
        %c: For characters.
        %b: For booleans.
        %n: For platform-specific line separators. 
        %s: For strings.
        */



    }
    
}
